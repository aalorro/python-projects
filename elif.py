from random import randint

num = randint(1, 7)

if num == 1:
    print("Random number is " + str(num) + " which is I in Roman numeral.")
elif num == 2:
    print("Random number is " + str(num) + " which is II in Roman numeral.")
elif num == 3:
    print("Random number is " + str(num) + " which is III in Roman numeral.")
elif num == 4:
    print("Random number is " + str(num) + " which is IV in Roman numeral.")
elif num == 5:
    print("Random number is " + str(num) + " which is V in Roman numeral.")
elif num == 6:
    print("Random number is " + str(num) + " which is VI in Roman numeral.")
else:
    print("Random number is " + str(num) + " which is VII in Roman numeral.")
